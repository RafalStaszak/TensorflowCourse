words = ['Alice', 'has', 'a', 'cat']

print('The list of single words in an array:')
for word in words:
    print(word)

print('The list of consecutive numbers')
for i in range(5):
    print(i)

print("Total sum of number from 10 to 20:")
sum = 0

for i in range(10,21):
    sum = sum + i

print(sum)



